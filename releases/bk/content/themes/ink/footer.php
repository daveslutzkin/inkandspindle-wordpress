      document.body.style.minWidth = '';
    }

    // Heuristic summary
    const summary = [];
    if (!mm('(min-width:1200px)') && approxZoomPct >= 140 && innerWidth>=1100===false) {
      summary.push('Likely Windows DPI/breakpoint (<1200px effective).');
