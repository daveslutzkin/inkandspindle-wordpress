(function(){
  const Q = new URLSearchParams(location.search);
  if (!Q.has('diag')) return;

  const log = (...a)=>console.log('%cI&S diag','color:#3a5;font-weight:600;',...a);
  const warn = (...a)=>console.warn('%cI&S diag','color:#a53;font-weight:600;',...a);
  const err  = (...a)=>console.error('%cI&S diag','color:#c22;font-weight:700;',...a);
  const wait = ms=>new Promise(r=>setTimeout(r,ms));
  const mm = q => matchMedia(q).matches;

  // ---- 0) Environment + zoom/breakpoints
  const vv = window.visualViewport;
  const approxZoomPct = Math.round((outerWidth / innerWidth) * 100); // Windows DPI tells on itself here
  const bpList = [480, 768, 1024, 1120, 1200, 1280, 1440];
  const breakpoints = Object.fromEntries(bpList.map(bp => [`≥${bp}`, mm(`(min-width:${bp}px)`)]));
  console.group('%cI&S diag ▸ Environment', 'font-weight:bold;');
  console.table([{
    ua: navigator.userAgent,
    chrome_major: (navigator.userAgent.match(/Chrome\/(\d+)/)||[])[1] || 'unknown',
    platform: navigator.platform,
    dpr: devicePixelRatio,
    viewport: `${innerWidth}×${innerHeight}`,
    visualViewport: vv ? `${vv.width.toFixed(1)}×${vv.height.toFixed(1)} @${vv.scale}` : 'n/a',
    approxZoomPct,
    no_js: document.documentElement.classList.contains('no-js'),
    js: document.documentElement.classList.contains('js')
  }]);
  console.table([breakpoints]);
  console.groupEnd();

  // ---- 1) Robust nav/header finder (works with many WP themes)
  function findNav(){
    const tests = [
      'header nav', 'nav[role="navigation"]', 'nav[aria-label*="primary" i]',
      '#site-navigation', '.site-navigation', '.main-navigation', '.primary-navigation',
      'ul.menu', 'ul[id^="menu-"]', 'nav ul'
    ];
    for (const sel of tests) {
      const el = document.querySelector(sel);
      if (el && (el.tagName==='NAV' || el.querySelectorAll('a').length >= 4)) return {el, sel};
    }
    // heuristic fallback: most-linked list near top
    const candidates = [...document.querySelectorAll('header ul, nav ul, .menu, [class*="nav"] ul')];
    let best = null, bestScore = 0;
    for (const ul of candidates) {
      const rect = ul.getBoundingClientRect();
      const links = ul.querySelectorAll('a').length;
      const score = links - (rect.top/100); // prefer high & link-dense
      if (links >= 4 && score > bestScore) { best = ul; bestScore = score; }
    }
    if (best) return {el: best.closest('nav') || best, sel: '(heuristic)'};
    return {el:null, sel:null};
  }

  function dumpNav(label){
    const found = findNav();
    if (!found.el) return warn('Nav not found');
    const el = found.el;
    const sel = found.sel;
    const ul = el.matches('ul') ? el : el.querySelector('ul');
    const cs  = getComputedStyle(el);
    const csU = ul ? getComputedStyle(ul) : null;
    console.group(`I&S diag ▸ Nav snapshot (${label}) sel=${sel}`);
    console.table([{
      nav_node: el.tagName.toLowerCase(),
      nav_display: cs.display, nav_flex: cs.display.includes('flex'),
      ul_display: csU ? csU.display : '(none)',
      ul_list_style: csU ? csU.listStyleType : '(none)',
      ul_gap: csU ? csU.gap : '(none)'
    }]);
    console.groupEnd();
  }

  // ---- 2) Script failure traps (early parse/runtime)
  window.addEventListener('error', e => {
    if (e.target && e.target.tagName === 'SCRIPT') {
      warn('SCRIPT load failed', e.target.src || '(inline)');
    } else {
      err('JS runtime', {msg:e.message, src:e.filename, line:e.lineno, col:e.colno});
    }
  });
  window.addEventListener('unhandledrejection', e => {
    err('Promise rejection', e.reason);
  });

  // ---- 3) CSS/JS resource sanity (same-origin only)
  async function fetchCheck(url){
    try {
      const r = await fetch(url + (url.includes('?')?'&':'?') + 'diag_bust=' + Date.now(), {cache:'reload', credentials:'same-origin'});
      const ct = r.headers.get('content-type');
      const ce = r.headers.get('content-encoding');
      let looksJS=false, looksCSS=false, looksHTML=false, head='';
      try {
        const text = await r.text();
        head = (text||'').slice(0,200);
        looksJS = /(?:function|=>|var |let |const |class |\/\*|\n)/.test(head);
        looksCSS = /{|@media|@font-face|:root/.test(head);
        looksHTML= /^\s*<!doctype html/i.test(text) || /<html[\s>]/i.test(text);
      } catch(_){}
      return {status:r.status, ok:r.ok, content_type:ct, content_encoding:ce, looksJS, looksCSS, looksHTML, head};
    } catch (e) {
      return {status:'ERR', ok:false, error:String(e)};
    }
  }

  async function resourceAudit(){
    const scripts = [...document.scripts].map(s=>s.src).filter(Boolean).filter(h=>new URL(h,location.href).origin===location.origin);
    const styles  = [...document.querySelectorAll('link[rel="stylesheet"]')].map(l=>l.href).filter(Boolean).filter(h=>new URL(h,location.href).origin===location.origin);
    const uniq = (a)=>[...new Set(a)];
    console.group('%cI&S diag ▸ Resource checks', 'font-weight:bold;');
    for (const href of uniq(styles.slice(0,3))) {
      const r = await fetchCheck(href);
      console.log('CSS', href, r);
      if (r.ok && r.content_type && !/text\/css/i.test(r.content_type)) warn('⚠︎ CSS wrong MIME', href, r.content_type);
      if (r.ok && r.looksHTML) warn('⚠︎ CSS is HTML (WAF/interstitial?)', href);
    }
    for (const href of uniq(scripts.slice(0,5))) {
      const r = await fetchCheck(href);
      console.log('JS ', href, r);
      if (!r.ok || r.status==='ERR') warn('⚠︎ JS fetch failed', href, r);
      if (r.ok && r.looksHTML) warn('⚠︎ JS is HTML (blocked/redirect)', href);
    }
    console.groupEnd();
  }

  // ---- 4) Diagnosis summary after load
  window.addEventListener('load', async () => {
    dumpNav('preload');
    await resourceAudit();
    await wait(50);

