	<div class="narrow-copy p_c_2 -m">
		<h3 class="mini-heading">Skin Contact</h3>
Up to 25% of the total weight of conventionally grown and processed fabrics is comprised of chemicals. That's worth pondering for a second. That shirt you might be wearing is <a href="https://www.twosistersecotextiles.com/pages/why-do-you-say-your-fabrics-are-safe-what-do-you-mean-by-safe">less than 75% cotton</a>, and the rest is a lovely combination of chemicals that help soften, colour, brighten or flame retard the fabric. These chemicals can be <a href="http://www.twosistersecotextiles.com/pages/i-m-not-going-to-eat-the-fabric-what-s-the-problem">absorbed into your body</a> by direct contact with your skin. The same applies to the couch that your child likes to lie on or the pillow case you sleep on, moreso if the item isn't made with natural fibres to begin with.
<br/><br/>
		<h3 class="mini-heading">Breathing</h3>
Harmful chemicals used in traditional textile processing evaporate into our homes over quite a long period of time, well beyond the point that any suspicion raising "new" smell has gone away. Carpets, couches, soft furnishings and window furnishings are <a href="https://www.custommade.com/blog/off-gassing/">just some of the contributors</a>, triggering a range of common health issues. Every small measure you can make to remove synthetic or traditionally processed textiles from your home is beneficial.
<br/><br/>
On the contrary, GOTS or Oeko-Tex certified fabrics contain no chemicals harmful to human health or our environment. All Ink & Spindle fabrics are either GOTS or Oeko-Tex certified.
