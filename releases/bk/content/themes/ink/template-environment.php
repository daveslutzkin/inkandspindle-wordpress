<h3 class="mini-heading">CERTIFIED BELGIAN LINEN</h3>
All of our linens are Certified Belgian Linens. There is a significant difference between Belgian Linen and linens otherwise advertised as ‘French Flax Linen’, ‘European Flax Linen’ or even ‘Belgian Flax Linen’. Certified Belgian Linen is not only made using flax from the European flax growing belt, but also milled in Belgium to the highest standards. Most other types of linen go through the spinning, weaving, dyeing & washing processes in countries such as China & India. And that's where a variety of issues with regards to quality, environment and ethics unfortunately come into play.<br/><br/>

Our mill is one of the oldest in Belgium, has the Belgian Linen & Masters of Linen certifications and is also Carbon Neutral. It's a long way to go to source our fabrics, but unfortunately due to Australia's declining woven textile industry we need to source further field to ensure we're printing our timeless designs on the best quality basecloths; ones that can be loved and lived with for a long time.<br/><br/>	

<h3 class="mini-heading">ECO-LINEN & OEKO-TEX</h3>
Linen is naturally a highly sustainable fibre, requiring no irrigation and few or zero fertilisers or pesticides. All parts of the flax plant are used with zero waste. Our Flax, Oatmeal and Natural coloured linens are classed as 'eco-linen', having undergone no dyeing treatments and feature the typical flax colours given to them by nature. Tests on all our linens have shown no chemical residues, making them <a href="https://www.oeko-tex.com/media/downloads/Factsheet_OETS_100_EN.pdf">Oeko-Tex Standard 100 class IV</a> certified as well.<br/><br/>
	   
<h3 class="mini-heading">GOTS</h3>
The Global Organic Textile Standard (GOTS) is the worldwide leading textile processing standard for organic fibres, including ecological and social criteria, backed up by independent certification of the entire textile supply chain. You can find more detailed information on the <a href="http://www.global-standard.org/">GOTS website</a>. GOTS certification applies to our organic cotton/hemp blend fabrics and our 100% hemp fabrics. Our inks are also GOTS compliant.<br/><br/>


